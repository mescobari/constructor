            let arrayItemsFiltered = [];
            for (let i = 0; i < arrayRequerimientoRelacion.length; i++) {
                if (arrayRequerimientoRelacion[i].requerimiento_id === this.jsonData.requerimiento_id) {
                    for (let j = 0; j < responsePlanillaItem.length; j++) {
